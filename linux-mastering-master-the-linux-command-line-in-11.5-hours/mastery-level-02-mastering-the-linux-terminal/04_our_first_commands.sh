#################### Basic Commands ####################
# echo command
echo Hello 
echo echoooooooooooooooooooooo

# cal command 
cal
cal 2017
cal -y

# date command
date 

#################### Clear Screen ####################
# clear command to clear screen
# Can also use CTRL + l shortcut 
clear 

#################### Command History ####################
# Use up and down arrows to cycle back through commands already run 
# Or view entire history with history command
history 

# To run specific command from history
!1 

# Run the most recent command 
!! 

# Clear history, then writes those changes to history file to make them permanent 
history -c; history -w

# Close terminal with exitcommand or CTRL + d 
exit 